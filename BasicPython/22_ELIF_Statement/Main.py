# ELIF = Else IF Statement

nama = input("Nama kamu siapa? ")

if nama == "ucup": # kondisi 1
    print("Hai ganteng beut!") # aksi true 1
elif nama == "otong": # kondisi 2
    print("Hai si kece banget!") # aksi true 2
elif nama == "mario": # kondisi 3
    print("Hai humoris ya!") # aksi true 3
else:
    print("wah gak kenal nih!") # aksi false

print("akhir dari program")